def add(num1, num2):
    """Adds two numbers together"""
    return num1 + num2

def subtract(num1, num2):
    """Subtracts num2 from num1"""
    return num1 - num2

def multiply(num1, num2):
    """multiply num2 from num1"""
    return num1 * num2

def divide(num1, num2):
    """divides num2 from num1"""
    return num1/ num2

def square(num1):
    """Squares num1"""
    return num1**2

def cube(num1):
    """cubes num1"""
    return num1**3

def power(num1, num2):
    """Raises num1 to the power of num2"""
    return num1**num2

def mod(num1, num2):
    """Returns the remainder when num1 is divided by num2"""
    return num1 % num2
